void main() {
    // Adição +
    // Subtração -
    // Multiplicação *
    // Divisão /
    // Resto da divisão %
    // Precedencia ()

    // Numeros int, double

    int numero1 = 10;
    int numero2 = 3;

    int adicao = numero1 + numero2;
    int sub = numero1 - numero2;
    int multi = numero1 * numero2;
    double quo = numero1 / numero2;
    int resto = numero1 % numero2;
    double precedencia = numero1 / (numero1 + numero2);

    print(adicao);
    print(sub);
    print(multi);
    print(quo);
    print(resto);
    print(precedencia);
}